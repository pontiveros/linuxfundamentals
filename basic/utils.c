void exchangeValues(int *a, int *b) {
    int aux = *a;
    *a = *b;
    *b = aux;
}

void sortByExchange(int items[], int len) {
    int i, j = 0;
    for (i = 0; i < (len -1); i++) {
        for (j = i + 1; j < len; j++) {
            if (items[i] > items[j]) {
                exchangeValues(&items[i], &items[j]);
            }
        }
    }
}

